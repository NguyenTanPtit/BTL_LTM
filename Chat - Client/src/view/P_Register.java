@Override
    public void update(Observable o, Object arg) {
        Response response = (Response) arg;
        System.out.println(response.getResultType());
        if (response.getResultType().equals(ResultFlags.ERROR)) {
            JOptionPane.showMessageDialog(null, response.getContent(), "Username already exist", JOptionPane.ERROR_MESSAGE);
            btnSignup.setEnabled(true);
        } else if (response.getActionType().equals(ActionFlags.REGISTER)) {
            JOptionPane.showMessageDialog(this, "Resgister success");
           PublicEvent.getInstance().getEventLogin().goLogin();
        }
    }
